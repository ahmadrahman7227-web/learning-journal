// Number (bilangan bulat)
console.log(7); // 7
console.log(-60) // -60

// Number (bilangan desimal)
console.log(3.14); // 3.14
console.log(1.25); //1.25

// String
console.log('Hello World'); // Hello World
console.log("Alif"); // Alif

// Boolean
let isLoggedIn = true;
let hasAccess = false;

console.log(isLoggedIn); // true
console.log(hasAccess); // false

// Undefined
let age;
console.log(age); // undefined

// Object
let person = { name: "Alice",  age: 30};
console.log(person.name); // Alice
console.log(person.age); // 30

Symbol
let id = symbol('userId');
console.log(id); // symbol(userId)

// BigInt
let bigNumber = 123456789n; // n = memprediksi atau memberitahu yang akan muncul angka.
console.log(bigNumber); 

// Array
let buah = ["Apel", "Manggis", "Semangka"];
console.log(buah);

// Deklarasi let
let score = 100;
console.log(score); // 100
score = 90;
console.log(score); // 90

// Deklarasi const
const name = "Bela";
console.log(name);

